## adding numbers from 1 to 10 using for loop

sum = 0
for i in range(1,11):
    sum = sum + i
print(sum)

## adding numbers from 1 to 10 using while loop

i = 1
sum = 0 
while i <= 10:
    sum = sum + i
    i = i + 1
print(sum)

